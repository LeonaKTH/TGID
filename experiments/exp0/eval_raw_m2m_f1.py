import argparse
import json
import os
import sys
from collections import Counter, defaultdict
from pathlib import Path


ROOT = Path(__file__).resolve().parents[2]
if str(ROOT) not in sys.path:
    sys.path.insert(0, str(ROOT))


def sanitize_omp_num_threads():
    omp_num_threads = os.environ.get("OMP_NUM_THREADS")
    if omp_num_threads is None:
        return
    try:
        if int(omp_num_threads) > 0:
            return
    except ValueError:
        pass
    os.environ["OMP_NUM_THREADS"] = "1"


def collect_targets(doc):
    arg_targets = defaultdict(set)
    for event_index, event in enumerate(doc.events):
        event_type = event.get("EventType")
        for role, value in event.items():
            if role == "EventType" or value is None:
                continue
            values = value if isinstance(value, list) else [value]
            for one_value in values:
                if one_value is None:
                    continue
                arg_targets[str(one_value)].add((event_type, role, event_index))
    return arg_targets


def doc_bucket(doc):
    arg_targets = collect_targets(doc)
    has_cross_type = False
    has_same_type_multi_instance = False
    has_cross_role = False
    has_m2m_all = False

    for targets in arg_targets.values():
        if len(targets) <= 1:
            continue
        has_m2m_all = True
        event_types = {target[0] for target in targets}
        roles = {target[1] for target in targets}
        instances_per_type = Counter(target[0] for target in {(x[0], x[2]) for x in targets})
        if len(event_types) > 1:
            has_cross_type = True
        if any(count > 1 for count in instances_per_type.values()):
            has_same_type_multi_instance = True
        if len(roles) > 1:
            has_cross_role = True

    if has_m2m_all:
        return {
            "m2m_all": True,
            "cross_type": has_cross_type,
            "same_type_multi_instance": has_same_type_multi_instance,
            "cross_role": has_cross_role,
        }
    return {
        "m2m_all": False,
        "cross_type": False,
        "same_type_multi_instance": False,
        "cross_role": False,
    }


def build_subset_doc_ids(processor, split):
    docs = getattr(processor, "{}_docs".format(split))
    subset_ids = {
        "overall": set(),
        "m2m_all": set(),
        "non_m2m": set(),
        "cross_type": set(),
        "same_type_multi_instance": set(),
        "cross_role": set(),
    }
    for doc in docs:
        subset_ids["overall"].add(doc.doc_id)
        bucket = doc_bucket(doc)
        if bucket["m2m_all"]:
            subset_ids["m2m_all"].add(doc.doc_id)
        else:
            subset_ids["non_m2m"].add(doc.doc_id)
        for name in ["cross_type", "same_type_multi_instance", "cross_role"]:
            if bucket[name]:
                subset_ids[name].add(doc.doc_id)
    return subset_ids


def score_raw_subsets(raw_results, subset_ids, metric):
    output = {}
    for name, doc_ids in subset_ids.items():
        subset = [row for row in raw_results if row["doc_id"] in doc_ids]
        if not subset:
            output[name] = {"doc_num": 0, "event": {}}
            continue
        _, score = metric.the_score_fn(subset)
        output[name] = {
            "doc_num": len(subset),
            "event": score.get("event", {}),
        }
    return output


def main():
    parser = argparse.ArgumentParser(description="Evaluate raw.pt on M2M document subsets.")
    parser.add_argument("--raw-result", required=True, help="raw.pt generated by run.py eval/train evaluation.")
    parser.add_argument("--data-name", required=True, help="Dataset name under Data/.")
    parser.add_argument("--split", default="test", choices=["train", "dev", "test"])
    parser.add_argument("--device", default="cpu")
    parser.add_argument("--output", default="outputs/exp0/raw_m2m_f1.json")
    args = parser.parse_args()

    sanitize_omp_num_threads()
    import torch
    from procnet.data_processor.DocEE_processor import DocEEProcessor
    from procnet.data_preparer.DocEE_preparer import DocEEPreparer
    from procnet.metric.DocEE_metric import DocEEMetric
    from run import get_config

    config = get_config()
    config.data_name = args.data_name
    config.device = torch.device(args.device)

    processor = DocEEProcessor(args.data_name)
    subset_ids = build_subset_doc_ids(processor, args.split)
    preparer = DocEEPreparer(config=config, processor=processor)
    metric = DocEEMetric(preparer=preparer)
    raw_results = torch.load(args.raw_result, map_location="cpu")
    output = score_raw_subsets(raw_results, subset_ids, metric)

    output_path = Path(args.output)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, "w", encoding="utf-8") as file:
        json.dump(output, file, ensure_ascii=False, indent=2)
    print(json.dumps(output, ensure_ascii=False, indent=2))


if __name__ == "__main__":
    main()
